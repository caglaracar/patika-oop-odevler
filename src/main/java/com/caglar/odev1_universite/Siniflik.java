package com.caglar.odev1_universite;

public class Siniflik {
    private String ad;
    private int kapasite;

    public Siniflik(String ad, int kapasite) {
        this.ad = ad;
        this.kapasite = kapasite;
    }

    public String getAd() {
        return ad;
    }

    public int getKapasite() {
        return kapasite;
    }
}
